    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        NumberFormat myFormat = NumberFormat.getInstance();
        myFormat.setGroupingUsed(true);
        if(taxable.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Pakilagay ang taunang kita.");
        }
        else{
        double i=Double.parseDouble(taxable.getText());
        taxable.setText(""+(myFormat.format(i)));
        if (i<250000){
            taxes.setText("0.00");
            i=Math.round(i*100.0)/100.0;
            income.setText(""+(myFormat.format(i)));  
        }
        else if (i>250000 && i<=400000){
            double rt=i-250000;
            double tx=rt*.20;
            tx=Math.round(tx*100.0)/100.0;
            taxes.setText(""+(myFormat.format(tx)));
            double buo=i-tx;
            buo=Math.round(buo*100.0)/100.0;
            income.setText(""+(myFormat.format(buo)));
        }
        else if (i>400000 && i<=800000){
            double rt=i-400000;
            double tx=(rt*.25)+30000;
            tx=Math.round(tx*100.0)/100.0;
            taxes.setText(""+(myFormat.format(tx)));
            double buo=i-tx;
            buo=Math.round(buo*100.0)/100.0;
            income.setText(""+(myFormat.format(buo)));
            
        }
        else if (i>800000 && i<=2000000){
            double rt=i-800000;
            double tx=(rt*.30)+130000;
            tx=Math.round(tx*100.0)/100.0;
            taxes.setText(""+(myFormat.format(tx)));
            double buo=i-tx;
            buo=Math.round(buo*100.0)/100.0;
            income.setText(""+(myFormat.format(buo)));
        }
        else if (i>2000000 && i<=8000000){
            double rt=i-2000000;
            double tx=(rt*.32)+490000;
            tx=Math.round(tx*100.0)/100.0;
            taxes.setText(""+(myFormat.format(tx)));
            double buo=i-tx;
            buo=Math.round(buo*100.0)/100.0;
            income.setText(""+(myFormat.format(buo)));
        }
        else if (i>8000000){
            double rt=i-8000000;
            double tx=(rt*.35)+2410000;
            tx=Math.round(tx*100.0)/100.0;
            taxes.setText(""+(myFormat.format(tx)));
            double buo=i-tx;
            buo=Math.round(buo*100.0)/100.0;
            income.setText(""+(myFormat.format(buo)));
        }}
    }//GEN-LAST:event_jButton1ActionPerformed

