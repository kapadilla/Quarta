    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if(t3.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Pakilagay kung magkano sa peso ang gustong malaman ang katumbas.");
        }
        else{
        double amountToChange=Double.parseDouble(t3.getText());
        NumberFormat myFormat = NumberFormat.getInstance();
        myFormat.setGroupingUsed(true);
        t3.setText(""+(myFormat.format(amountToChange)));
        double amountChanged=0.0;
        switch(c2.getSelectedItem().toString())
        {
        case "England":amountChanged = amountToChange * British_Pound;break;
        case "European Union":amountChanged = amountToChange * European_Union; break;
        case "Indonesia":amountChanged = amountToChange * Indonesian_Rupiah;break;
        case "Japan":amountChanged = amountToChange * Japanese_Yen;break;
        case "Saudi":amountChanged = amountToChange * Saudi_Riyal;break;
        case "South Korea":amountChanged = amountToChange * SouthKorean_Won; break;
        case "USA":amountChanged = amountToChange * US_Dollar; break;
	default:amountChanged = amountToChange * 0.0;
   }
        
        double Changed=Math.round(amountChanged*100.0)/100.0;
         
        t2.setText(""+(myFormat.format(Changed)));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

