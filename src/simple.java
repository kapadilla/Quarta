    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        NumberFormat myFormat = NumberFormat.getInstance();
        myFormat.setGroupingUsed(true);
        if(pri.getText().trim().isEmpty()||rate.getText().trim().isEmpty()||time.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Pakilagay ang principal, rate and time.");
        }
        else{
        double p=Double.parseDouble(pri.getText());
        double r=Double.parseDouble(rate.getText());
        double t=Double.parseDouble(time.getText());
        p=Math.round(p*100.0)/100.0;
        pri.setText(""+(myFormat.format(p)));
        r=Math.round(r*100.0)/100.0;
        rate.setText(""+(myFormat.format(r)));
        t=Math.round(t*100.0)/100.0;
        time.setText(""+(myFormat.format(t)));
                
        double si=p*r*t/100;
        double fa=si+p;
       
        si=Math.round(si*100.0)/100.0;
        sim.setText(""+(myFormat.format(si)));
        fa=Math.round(fa*100.0)/100.0;
        fin.setText(""+(myFormat.format(fa)));  
        }
    }//GEN-LAST:event_jButton1ActionPerformed

