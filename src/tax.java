    private void taxableKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_taxableKeyTyped
        // TODO add your handling code here:
          if(!validate(evt.getKeyChar())){ 
            getToolkit().beep();
            evt.consume();
        }
        
        if(evt.getKeyChar()==KeyEvent.VK_DECIMAL || evt.getKeyChar()==KeyEvent.VK_PERIOD){
            
            String field = taxable.getText(); 
            int index = field.indexOf("."); 
            if(!(index==-1)){
                getToolkit().beep();
                evt.consume(); 
            }
        }
    }     
    //determine if keytyped is a valid input
     private boolean validate(char ch){
        
            if(!(Character.isDigit(ch)
                    || ch==KeyEvent.VK_DELETE  
                    || ch==KeyEvent.VK_DECIMAL 
                    || ch==KeyEvent.VK_PERIOD 
                    )|| ch=='n'){
                return false;        
            }
            return true; 
    }//GEN-LAST:event_taxableKeyTyped
       
