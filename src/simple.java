    private void priKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_priKeyTyped
        // TODO add your handling code here:
        if(!validate(evt.getKeyChar())){ 
            getToolkit().beep();
            evt.consume();
        }
        
        if(evt.getKeyChar()==KeyEvent.VK_DECIMAL || evt.getKeyChar()==KeyEvent.VK_PERIOD){
            
            String field = pri.getText(); 
            int index = field.indexOf("."); 
            if(!(index==-1)){
                getToolkit().beep();
                evt.consume(); 
            }
        }
    }//GEN-LAST:event_priKeyTyped

