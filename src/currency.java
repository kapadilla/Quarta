    private void t2KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_t2KeyTyped
        if(!validate(evt.getKeyChar())){
            getToolkit().beep();
            evt.consume();
        }

        if(evt.getKeyChar()==KeyEvent.VK_DECIMAL || evt.getKeyChar()==KeyEvent.VK_PERIOD){

            String field = t2.getText();
            int index = field.indexOf(".");
            if(!(index==-1)){
                getToolkit().beep();
                evt.consume();
            }
        }
        }

        private void discKeyTyped(java.awt.event.KeyEvent evt) {
            // TODO add your handling code here:
            if(!validate(evt.getKeyChar())){
                getToolkit().beep();
                evt.consume();
            }

            if(evt.getKeyChar()==KeyEvent.VK_DECIMAL || evt.getKeyChar()==KeyEvent.VK_PERIOD){

                String field = t2.getText();
                int index = field.indexOf(".");
                if(!(index==-1)){
                    getToolkit().beep();
                    evt.consume();
                }
            }
        }
        private boolean validate(char ch){

            if(!(Character.isDigit(ch)
                || ch==KeyEvent.VK_DELETE
                || ch==KeyEvent.VK_DECIMAL
                || ch==KeyEvent.VK_PERIOD
            )|| ch=='n'){
                return false;
            }
            return true;         // TODO add your handling code here:
    }//GEN-LAST:event_t2KeyTyped

